public enum OrderMechanism {

    /**
     * When placing a limit order, the customer specifies the price at which they are willing to buy or sell an asset.
     */
    LIMIT,

    /**
     * When placing a market order, the customer is willing to buy or sell an asset at the best available price.
     */
    MARKET

}
